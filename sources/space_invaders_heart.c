void heart_frame_set(
  char** pixels,
  unsigned char filled
) {
  const char* heart_frame = (
    (
      filled !=
      0x00
    )
    ? heart_frame_filled
    : heart_frame_unfilled
  );

  for (
    unsigned char index_y = (
      0x00
    );
    (
      index_y <
      heart_size_height
    );
    ++index_y
  ) {
    unsigned short int offset_heart = (
      index_y *
      heart_size_width
    );

    for (
      unsigned char index_x = (
        0x00
      );
      (
        index_x <
        heart_size_width
      );
      ++index_x
    ) {
      pixels[
        index_y
      ][
        index_x
      ] = (
        heart_frame[
          offset_heart +
          index_x
        ]
      );
    }
  }
}
