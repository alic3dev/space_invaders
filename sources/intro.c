void intro_initialize(
  struct intro* intro
) {
  struct math_c_vector2_unsigned_int size_intro = {
    .x = (
      size_intro_width
    ),
    .y = (
      size_intro_height
    )
  };

  cexil_sprite_initialize(
    &intro->sprite,
    &size_intro
  );

  for (
    unsigned char index_y = (
      0x00
    );
    (
      index_y <
      size_intro.y
    );
    ++index_y
  ) {
    for (
      unsigned char index_x = (
        0x00
      );
      (
        index_x <
        size_intro.x
      );
      ++index_x
    ) {
      intro->sprite.pixels[
        index_y
      ][
        index_x
      ] = (
        pixels_intro[
          index_y
        ][
          index_x
        ]
      );
    }
  }

  intro->size_render.y = (
    0x00
  );
  
  intro->size_render.x = (
    intro->sprite.size.x
  );

  cexil_sprite_render_size_set(
    &intro->sprite,
    &intro->size_render
  );
}
